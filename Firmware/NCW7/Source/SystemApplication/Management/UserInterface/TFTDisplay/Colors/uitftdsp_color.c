/*---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 * @brief:	Sets the current color..
 * @param:	vulColor - Color to set to.
 * @retval:	None.
 *-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
void							UITFTDSP_COLOR__SetCurrentColor(uint32_t vulColor)
{
	// Assign RGB colors
	uint8_t	ucColorR = (uint8_t)( vulColor >> 16 );
	uint8_t	ucColorG = (uint8_t)( vulColor >> 8  );
	uint8_t	ucColorB = (uint8_t)( vulColor >> 0  );
	// Set the current color
	FT800_COPRCCMD__CPDatQueue__Value( FT800_DISPLIST__DLCmd__COLOR_RGB( ucColorR, ucColorG, ucColorB ) );

	return;
}
