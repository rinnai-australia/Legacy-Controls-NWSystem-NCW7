/*---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 * @brief:	Initialises data ready for processing once this state is entered.
 * @param:	None.
 * @retval:	None.
 *-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
void							NWS_DSPSET_CONTROL__InitBeforeStateEntry(void)
{
	NWS_DSPSET_USERIF_NC7__InitBeforeStateEntry(&mDisplaySettings);

	return;
}
/*---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 * @brief:	User interface update - Display Settings State.
 * @param:	vbStateJustEntered	- Indicates if this state has just been entered (first time in this fcn).
 * @retval:	None.
 *-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
void							NWS_DSPSET_CONTROL__UserifUpdate(bool vbStateJustEntered)
{
	// Init any data when state just entered
	if ( vbStateJustEntered ) { NWS_DSPSET_CONTROL__InitBeforeStateEntry(); }
	//
	// Clear any button press
	mDisplaySettings.UserAccess.Control.ButtonPressed = NWS_DSPSET_COMMON__CB__NONE_PRESSED;
	//
	// User inteface update
	NWS_DSPSET_USERIF_NC7__UpdateDisplay( &mDisplaySettings );
	NWS_DSPSET_USERIF_NC7__UpdateControl( &mDisplaySettings );
	//
	// Process any control actions
	switch ( mDisplaySettings.UserAccess.Control.ButtonPressed )
	{
		case NWS_DSPSET_COMMON__CB__BACK:
			// Back button
			// - Save updated user settings
			NVMIFMGR__BeginSaveProcess_UserSettings();
			// - Revert back to user preferences screen
			NWSUTILS__RevertConfigMenu_UserPreferences();
			break;

		default:
			if ( TFTDSPIF__bNoTouchTimeout() ) { NWSUTILS__CancelConfigMenu(); }
			break;
	}

	return;
}
