/*---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 * @brief:	Initialises data ready for processing once this state is entered.
 * @param:	None.
 * @retval:	None.
 *-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
void							NWS_CLNSCN_CONTROL__InitBeforeStateEntry(void)
{
	// When cleaning the screen keep backlight at full brightness
	UITFTDSP__Backlight_KeepAtFullBrightness( true );
	// Start 30 second timer
	GENTIMDT__StartTimer_TB_1SEC( 31, &mCleanScreen.AutoExitDelayTimingCounter );

#if (BUILD_INFO__NCX_DEVICE == COM_DEFN__NCX_DEVICE__NC7)
	NWS_CLNSCN_USERIF_NC7__InitBeforeStateEntry( &mCleanScreen );
#endif
	return;
}
// APPLICATION LEVEL FUNCTION DEFINITIONS
/*---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 * @brief:	User interface update - Clean Screen state.
 * @param:	vbStateJustEntered - Indicates if this state has just been entered (first time in this fcn).
 * @retval:	None.
 *-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
void							NWS_CLNSCN_CONTROL__UserifUpdate(bool vbStateJustEntered)
{
	// Init any data when state just entered
	if ( vbStateJustEntered ) { NWS_CLNSCN_CONTROL__InitBeforeStateEntry(); }
	//
	// Clear any button press
	mCleanScreen.UserAccess.Control.ButtonPressed = NWS_CLNSCN_COMMON__CB__NONE_PRESSED;
	//
	// User inteface update
#if (BUILD_INFO__NCX_DEVICE == COM_DEFN__NCX_DEVICE__NC7)
	NWS_CLNSCN_USERIF_NC7__UpdateDisplay( &mCleanScreen );
	NWS_CLNSCN_USERIF_NC7__UpdateControl( &mCleanScreen );
#endif
	//
	// Process any control actions - Just check for timeout
	if ( mCleanScreen.AutoExitDelayTimingCounter.ulValue == 0 )
	{
		UITFTDSP__Backlight_KeepAtFullBrightness( false );

		NWSUTILS__RevertConfigMenu();
	}

	return;
}
