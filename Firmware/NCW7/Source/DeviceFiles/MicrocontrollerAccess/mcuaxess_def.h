//	System Clocks & Pre-Scaling
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
#define			PRE_SCALER_AHB				1							// AHB Bus Pre-Scaling					- 2
#define			PRE_SCALER_APB1				2							// APB1 Bus Pre-Scaling					- 2
#define			PRE_SCALER_APB2				1							// APB2 Bus Pre-Scaling					- 1

#define			PRE_SCALER_ADC				8							// ADC Pre-Scaling						- 2

#define			HSECLOCK					8000000						// External clock frequency				- 8MHz
#define			PLLCLK						(HSECLOCK*9)				// PLL -> HSECLOCK * 9 = 72MHz			- 72MHz

#define			SYSCLK						PLLCLK						// SYSCLK								- 72MHz

#define			HCLK						(SYSCLK/PRE_SCALER_AHB)		// HCLK									- 72MHz
#define			PCLK1						(HCLK/PRE_SCALER_APB1)		// PCLK1								- 36MHz
#define			PCLK2						(HCLK/PRE_SCALER_APB2)		// PCLK2								- 72MHz
#define			TIM2CLK						PCLK1						// TIM2CLK								- 36MHz
#define			ADCCLK						(PCLK2/PRE_SCALER_ADC)		// ADCCLK								- 6MHz
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
//	System Interrupt Priority Levels
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
#define			MCUAXESS_DEF__IP_SYSTICK	14							// 10ms system tick timer
#define			MCUAXESS_DEF__IP_RTC_SECI	13							// RTC Seconds Interrupt Priority

#define			MCUAXESS_DEF__IP_USART1		12							// USART1 Interrupt Priority (USB to Serial)
#define			MCUAXESS_DEF__IP_UART5		13							// UART5 Interrupt Priority  (TWB)

#define			MCUAXESS_DEF__IP_SPI1_HI	13							// SPI1 interrupt		- FT800 SPI (Hi priority)
#define			MCUAXESS_DEF__IP_SPI1_LO	15							// SPI1 interrupt		- FT800 SPI (Lo priority)

#define			MCUAXESS_DEF__IP_I2C1_EV	13							// I2C1 EV Interrupt Priority
#define			MCUAXESS_DEF__IP_I2C1_ER	13							// I2C1 ER Interrupt Priority

#define			MCUAXESS_DEF__IP_TIM6		13							// TIM6 Interrupt Priority
